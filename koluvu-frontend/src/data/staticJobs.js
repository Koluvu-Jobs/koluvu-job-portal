// Function to get jobs with pagination and filtering
export const getJobsWithFilters = (filters = {}) => {
  let filteredJobs = [...staticJobs];

  // Apply filters
  if (filters.location) {
    filteredJobs = filteredJobs.filter((job) =>
      job.location.toLowerCase().includes(filters.location.toLowerCase())
    );
  }

  if (filters.employmentType) {
    filteredJobs = filteredJobs.filter(
      (job) => job.employmentType === filters.employmentType
    );
  }

  if (filters.company) {
    filteredJobs = filteredJobs.filter((job) =>
      job.company.toLowerCase().includes(filters.company.toLowerCase())
    );
  }

  if (filters.search) {
    filteredJobs = filteredJobs.filter(
      (job) =>
        job.jobTitle.toLowerCase().includes(filters.search.toLowerCase()) ||
        job.jobDescription.toLowerCase().includes(filters.search.toLowerCase())
    );
  }

  return {
    data: filteredJobs,
    total: filteredJobs.length,
  };
};
